
function getValueStay() {
    var run = document.getElementById("runAway");
    if(run.style.display == 'block')
       run.style.display = 'none';
    else
       run.style.display = 'block';
 }

 

function getValueRun() {
    var run = document.getElementById("runAway");
    if(run.style.display == 'block')
       run.style.display = 'none';
    else
       run.style.display = 'block';
 }

 